if __name__ == '__main__':
    try:
        os.mkdir("Cheats")
    except FileExistsError:
        pass

    # There are a lot of addresses, adding everything to one file would make things to hard to find in
    # Game Conqueror. Better to split it by character.
    for char in characters:
        character_name = char[0]
        base_address = char[1]
        # adding the name is actually redundant but helps double checking if everything is right in Game Conqueror
        # `" "*13` ist to work around a limitation of Game Conqueror that string types have no length
        # but use whatever length the value has
        cheats = [["=", False, f"{character_name} Name", base_address, 'string', " " * 13, True]]
        for o in offsets:
            cheat_value_name = o[0]
            cheat_offset = o[1]
            cheat_datatype = o[2]
            cheats.append(
                ["=", False, f"{character_name} {cheat_value_name}", base_address + cheat_offset, cheat_datatype, "0",
                 True])

        # print(cheats)
        with open(f'Cheats/{character_name} Cheats.json', mode='w') as json_file:
            json_file.write(json.dumps({'cheat_list': cheats}, indent=2))
